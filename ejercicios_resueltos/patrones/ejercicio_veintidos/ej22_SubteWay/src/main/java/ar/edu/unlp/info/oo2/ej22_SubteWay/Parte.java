package ar.edu.unlp.info.oo2.ej22_SubteWay;

public class Parte {
	
	private String nombre;
	private double precio;
	
	public Parte(String nombre, double precio) {
		this.nombre = nombre;
		this.precio = precio;
	}

	public String getNombre() {
		return nombre;
	}

	public double getPrecio() {
		return precio;
	}
	
}
